import { toast } from 'react-toastify';

// Toast notifications with auto-dismiss
export const notifyError = (message) => {
  if (typeof window !== 'undefined') {
    toast.error(message, {
      position: "top-right",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
    });
  }
};

